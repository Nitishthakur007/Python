def Float_fun(f1,f2,power):
    print("#Add")
    print(f1 + f2)
    print("#Subtract")
    print(f1 - f2)
    print("#Multiply")
    print(f1 * f2)
    print("#Divide")
    print(f2/f1)
    print("#Remainder")
    print(f1 % f2)
    print("#To_The_Power_of")
    print(f1 ** power)
    print("#Round")
    print(round((f1 ** power),4))



Float_fun(10.75,5.45,3)