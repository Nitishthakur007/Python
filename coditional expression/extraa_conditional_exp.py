# syntax
a = 22
if a > 2:
    print("greater than 2")  # run as independent

if a > 5:
    print("greater than 5")  # run as independent

if a > 100:
    print("greater than 100")
elif a > 10:
    print("greater than 10")

else:
    print("done")


# for comparing two variables

a = 22
b = 24
if a > b:
    print("greater than 2")  # run as independent

if a >= b:
    print("greater than 5")  # run as independent

if a <= b:
    print("greater than 100")
elif a > 10:
    print("greater than 10")

else:
    print("done")
